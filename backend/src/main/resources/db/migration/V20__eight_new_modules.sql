-- 8. Insurance and TPA Claims
CREATE TABLE IF NOT EXISTS insurance_providers (
    provider_id VARCHAR(36) PRIMARY KEY,
    provider_name VARCHAR(150) NOT NULL,
    provider_code VARCHAR(20) UNIQUE NOT NULL,
    provider_type ENUM('TPA','Insurance_Company','Government_Scheme','Corporate') NOT NULL,
    contact_person VARCHAR(100),
    mobile VARCHAR(15),
    email VARCHAR(100),
    address TEXT,
    claim_submission_mode ENUM('online_portal','email','courier','representative') NOT NULL,
    claim_portal_url VARCHAR(200),
    turnaround_days INT DEFAULT 30,
    coverage_policy TEXT,
    is_active TINYINT(1) DEFAULT 1,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS insurance_medicine_coverage (
    coverage_id VARCHAR(36) PRIMARY KEY,
    provider_id VARCHAR(36) NOT NULL,
    medicine_id BIGINT NOT NULL,
    medicine_name VARCHAR(150) NOT NULL,
    is_covered TINYINT(1) DEFAULT 1,
    coverage_percentage DECIMAL(5,2) DEFAULT 100.00,
    max_coverage_amount DECIMAL(10,2),
    coverage_notes TEXT,
    effective_from DATE NOT NULL,
    effective_to DATE,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uq_provider_medicine (provider_id, medicine_id),
    FOREIGN KEY (provider_id) REFERENCES insurance_providers(provider_id),
    FOREIGN KEY (medicine_id) REFERENCES medicines(id)
);

CREATE TABLE IF NOT EXISTS insurance_claims (
    claim_id VARCHAR(36) PRIMARY KEY,
    claim_number VARCHAR(30) UNIQUE NOT NULL,
    claim_date DATE NOT NULL,
    bill_id BIGINT NOT NULL,
    patient_id BIGINT NOT NULL,
    patient_name VARCHAR(100) NOT NULL,
    patient_uhid VARCHAR(30),
    insurance_provider_id VARCHAR(36) NOT NULL,
    insurance_policy_number VARCHAR(60) NOT NULL,
    tpa_name VARCHAR(100),
    total_bill_amount DECIMAL(12,2) NOT NULL,
    covered_amount DECIMAL(12,2) NOT NULL,
    non_covered_amount DECIMAL(12,2) NOT NULL,
    claimed_amount DECIMAL(12,2) NOT NULL,
    approved_amount DECIMAL(12,2),
    rejected_amount DECIMAL(12,2),
    rejection_reason TEXT,
    claim_status ENUM('draft','submitted','under_review','partially_approved','approved','rejected','settled') DEFAULT 'draft',
    submission_date DATE,
    approval_date DATE,
    settlement_date DATE,
    settlement_reference VARCHAR(60),
    claim_document_url TEXT,
    created_by BIGINT NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (bill_id) REFERENCES pharmacy_bills(id),
    FOREIGN KEY (patient_id) REFERENCES patients(id),
    FOREIGN KEY (insurance_provider_id) REFERENCES insurance_providers(provider_id),
    FOREIGN KEY (created_by) REFERENCES users(id),
    INDEX idx_claim_status (claim_status),
    INDEX idx_patient_id (patient_id),
    INDEX idx_provider_id (insurance_provider_id)
);

CREATE TABLE IF NOT EXISTS insurance_claim_line_items (
    claim_line_id VARCHAR(36) PRIMARY KEY,
    claim_id VARCHAR(36) NOT NULL,
    medicine_id BIGINT NOT NULL,
    medicine_name VARCHAR(150) NOT NULL,
    bill_line_item_id BIGINT,
    quantity INT NOT NULL,
    unit_price DECIMAL(10,2) NOT NULL,
    line_total DECIMAL(12,2) NOT NULL,
    is_covered TINYINT(1) NOT NULL,
    coverage_percentage DECIMAL(5,2),
    covered_amount DECIMAL(12,2) DEFAULT 0,
    non_covered_amount DECIMAL(12,2) DEFAULT 0,
    non_covered_reason VARCHAR(150),
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (claim_id) REFERENCES insurance_claims(claim_id) ON DELETE CASCADE,
    FOREIGN KEY (medicine_id) REFERENCES medicines(id)
